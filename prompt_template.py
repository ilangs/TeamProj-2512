# 광고 카피 프롬프트
def adcopy_prompt(topic, message, target) -> str:
    prompt = f'''
    # Role (역할 부여)
    당신은 칸 국제 광고제 수상 경력이 있는 15년 차 베테랑 카피라이터이자 마케팅 전문가입니다. 
    시청자의 첫 3초를 훔치고 결제까지 이어지게 만드는 고효율 광고 스크립트를 작성합니다.

    # Input Data (입력 내용)
    - 주제(제품/서비스): {topic}
    - 핵심 강점(USP): {message}
    - 타겟 시청자: {target}

    # Task Instructions (지시 사항)
    1. 영상 길이는 1분으로 구성 해 주세요.
    2. [Hook]: 타겟의 페인 포인트(Pain Point)를 건드리는 강렬한 질문이나 공감 멘트로 시작하세요.
    3. [Body]: 제품이 어떻게 문제를 해결하는지 기능(Feature)이 아닌 혜택(Benefit) 중심으로 설명하세요.
    4. [Social Proof]: 신뢰를 줄 수 있는 리뷰나 데이터 수치를 자연스럽게 녹이세요.
    5. [Closing]: 희소성이나 긴박감을 주어 지금 당장 행동하게 만드세요.

    # Output Format (출력 형식)
    1. 제목 앞에 "[광고 카피] " 출력 후 이어서 주제에 맞는 제목 만들어 붙여 주세요.
    2. 서로 다른 소구점(콘셉트)을 가진 3가지 버전의 스크립트를 제안하고,
       각 버전 끝에는 해당 카피의 의도(마케팅 전략)를 설명하세요.
    3. 가장 높은 전환율(ROAS)이 예상되는 1개를 골라 마케팅 심리학 관점에서 사유를 적어 주고,
       최종 선정 카피를 [BSET] 표시하고 본문 출력 해주면 [BEST] 이후 부분만 음성 파일로 변경 예정입니다.

    '''
    return prompt


# 유튜브 스크립트 프롬프트
def youtube_prompt(topic, length, tone, target) -> str:
    prompt = f'''
    # Role (역할 부여)
    당신은 100만 구독자를 보유한 전문 유튜브 콘텐츠 기획자이자 스크립트 작가입니다.
    시청자의 이탈을 막고 끝까지 보게 만드는 몰입감 있는 대본을 작성해야 합니다.

    # Input Data (입력 내용)
    - 주제: {topic}
    - 목표 영상 길이: {length}
    - 영상 톤앤매너: {tone}
    - 타겟 시청자: {target}

    # Task Instructions (지시 사항)
    위 정보를 바탕으로 아래 구조에 맞춰 유튜브 스크립트를 작성하세요.

    1. **[후킹(Hook)]**: 영상 시작 0~10초 사이. 시청자가 영상을 끄지 않도록 강렬한 질문, 충격적인 사실, 또는 결과 미리보기를 제시하세요.
    2. **[인트로]**: 오늘 다룰 내용이 무엇인지, 왜 이 영상을 끝까지 봐야 하는지 이득(Benefit)을 제시하세요.
    3. **[본론]**: 내용을 논리적으로 3~4가지 핵심 포인트로 나누어 설명하세요. 지루하지 않게 스토리텔링 기법을 사용하세요.
    4. **[결론 및 CTA]**: 내용을 요약하고, 구독/좋아요/댓글을 유도하는 멘트(Call To Action)를 자연스럽게 넣으세요.

    # Output Format (출력 형식)
    1. 제목 앞에 "[유튜브] " 출력 후 이어서 주제에 맞는 제목 만들어 붙여 주세요.
    2. [5가지 아이디어 추천] (간략히 제목과 컨셉만)
    3. [베스트 선정 및 추천 사유] (논리적 근거)
    4. [최종 선정 스크립트] 아래 표(Table) 형식으로 작성해 주세요. 300자 이내)
    | 타임코드(예상)   | 화면 설명 (비주얼 가이드)   | 내레이션/대사 (스크립트)   |
    |----------------|--------------------------|-------------------------|
    | 0:00-0:10      | 후킹 장면 설명             | 후킹 대사 작성            |
    | 0:10-0:30      | 인트로 장면 설명           | 인트로 대사 작성          |
    | ...            | ...                      | ...                     |
    | 마무리          | 결론 및 CTA 장면 설명      | 결론 및 CTA 대사 작성     |
    5. 썸네일 문구와 해시테그 10개 만들어 주세요.
    6. 결과물 하단에 최종 선정분 '내레이션/대사(스크립트)' 열만 [BSET] 표시하여 리스트로 변환해 주면
       [BEST] 이후 부분만 음성 파일로 변경 예정입니다. (대사 열 번호는 붙이지 말고 본문만 나열)
       
    '''
    return prompt


# SNS 포스팅 프롬프트
def sns_prompt(topic, channel, tone, target) -> str:
    prompt = f'''
    # Role (역할 부여)
    당신은 팔로워 50만 명을 보유한 SNS 전문 인플루언서이자 소셜 마케터입니다. 
    사람들이 무심코 피드를 내리다가도 멈춰서 '좋아요'를 누르고 '댓글'을 달게 만드는 매력적인 문구를 작성합니다.

    # Input Data (입력 내용)
    - 주제: {topic}
    - 타겟 시청자: {target}
    - 채널: {channel}
    - 톤 : {tone}
  
    # Task Instructions (지시 사항)
    1. 제목 앞에 "[SNS 포스팅] " 출력 후 이어서 주제에 맞는 제목 만들어 붙여 주세요.
    2. [첫 줄(Hook)]: 가장 중요합니다! 질문을 던지거나, 반전이 있는 문구로 호기심을 자극하세요.
    3. [본문]: 읽기 편하도록 문장을 짧게 끊고, 줄바꿈을 적절히 사용하세요. 너무 설명조가 되지 않도록 친근하게 작성하세요.
    4. [공감 및 소통]: 마지막에 "여러분은 어떠신가요?"와 같이 댓글을 유도하는 질문을 넣어주세요.
    5. [해시태그]: 관련성 높은 인기 해시태그와 틈새 해시태그를 10개 내외로 생성하세요.

    # Output Format (출력 형식)
    - 채널 성격에 맞는 3가지 다른 버전(감성형, 정보전달형, 유머형 등)을 제시하세요.
    - 각 버전별로 어울리는 이미지/영상 컨셉도 한 줄로 추천해 주세요.
    - 최종적으로 가장 반응이 좋을 것 같은 '베스트 포스팅' 1가지 선정 이유를 설명해 주고,
      [BSET] 표시하여 본문 출력 해주면 [BEST] 표시 이후 부분만 음성 파일로 변경할 예정입니다.
        
    '''
    return prompt